#!/usr/bin/env bash
# End-to-end open-source timing flow for dma_top:
#   SystemVerilog --(sv2v)--> Verilog --(Yosys+Liberty)--> gate netlist --(OpenSTA)--> timing report
#
# OpenSTA does NOT synthesize; it needs a mapped gate netlist + a Liberty
# library. Yosys does the mapping. Point LIB at any free standard-cell .lib
# (Nangate45 or SkyWater sky130 both work). Example:
#
#   LIB=/path/to/sky130_fd_sc_hd__tt_025C_1v80.lib ./run.sh
#
# Prereqs (Ubuntu):  sudo apt-get install yosys sv2v opensta
#   (or build OpenSTA from github.com/parallaxsw/OpenSTA; sv2v via
#    the zachjs/sv2v release binary if not packaged.)
set -euo pipefail
cd "$(dirname "$0")"

RTL=../../rtl
TOP=dma_top
BUILD=build
mkdir -p "$BUILD"

: "${LIB:?Set LIB to a standard-cell Liberty file, e.g. LIB=/path/sky130..._tt_...1v80.lib ./run.sh}"
[ -f "$LIB" ] || { echo "LIB not found: $LIB" >&2; exit 1; }

echo "== 1/3  sv2v: SystemVerilog -> plain Verilog =="
# csr.sv is generated by reggen; regenerate if you changed the YAML first.
sv2v --write="$BUILD/${TOP}.v" \
     "$RTL/csr.sv" "$RTL/dma_fifo.sv" "$RTL/dma_channel.sv" \
     "$RTL/dma_axi_mux.sv" "$RTL/dma_top.sv"

echo "== 2/3  Yosys: synthesize + map to $(basename "$LIB") =="
yosys -q -p "
    read_verilog $BUILD/${TOP}.v
    hierarchy -check -top ${TOP}
    synth -top ${TOP} -flatten
    dfflibmap -liberty $LIB
    abc -liberty $LIB
    opt_clean -purge
    write_verilog -noattr $BUILD/${TOP}_netlist.v
    stat -liberty $LIB
"

echo "== 3/3  OpenSTA: static timing analysis =="
LIB="$LIB" NETLIST="$BUILD/${TOP}_netlist.v" \
    sta -no_init -exit sta.tcl | tee "$BUILD/${TOP}_sta.rpt"

echo
echo "Report written to $BUILD/${TOP}_sta.rpt"
echo "Analyze it with:  sta-analyze $BUILD/${TOP}_sta.rpt   (from ../../../sta-analyzer)"
